#ifndef DSP_LOG10_INT_HPP_
#define DSP_LOG10_INT_HPP_

/* ------------------------------------------------------------------------- */
namespace dsp {
namespace integer {
/* ------------------------------------------------------------------------- */

constexpr inline int log10( unsigned long long v )
{
	return (v >= 10000000000000000000llu) ? 19 : (v >= 1000000000000000000llu) ? 18 :
		        (v >= 100000000000000000llu) ? 17 : (v >= 10000000000000000llu) ? 16 :
		        (v >= 1000000000000000llu) ? 15 : (v >= 100000000000000llu) ? 14 :
		        (v >= 10000000000000llu) ? 13 : (v >= 1000000000000llu) ? 12 :
		        (v >= 100000000000llu) ? 11 : (v >= 10000000000llu) ? 10 :
		        (v >= 1000000000llu) ? 9 : (v >= 100000000llu) ? 8 :
		        (v >= 10000000llu) ? 7 : (v >= 1000000llu) ? 6 :
		        (v >= 100000llu) ? 5 : (v >= 10000llu) ? 4 :
		        (v >= 1000u) ? 3 : (v >= 100u) ? 2 : (v >= 10u) ? 1u : 0u;
}

constexpr inline int log10(unsigned int v)
{
    return (v >= 1000000000u) ? 9 : (v >= 100000000u) ? 8 :
        (v >= 10000000u) ? 7 : (v >= 1000000u) ? 6 :
        (v >= 100000u) ? 5 : (v >= 10000u) ? 4 :
        (v >= 1000u) ? 3 : (v >= 100u) ? 2 : (v >= 10u) ? 1u : 0u;
}

/* ------------------------------------------------------------------------- */
}}
/* ------------------------------------------------------------------------- */
#endif /* LOG10_INT_HPP_ */
